package OrientadoaObjeto;

class Calculadora {
  double numberOne;
  double numberTwo;

  double sum() {
    return numberOne + numberTwo;
  }

  double subtraction() {
    return numberOne - numberTwo;
  } 

  double multiplication() {
    return numberOne * numberTwo;
  } 

  double division() {
    return numberOne / numberTwo;
  } 

}
